	AREA RESET, DATA, READONLY
	EXPORT __Vectors
__Vectors
	DCD 0x40001000
	DCD Reset_Handler
	ALIGN
	AREA mycode, CODE, READONLY
	ENTRY
	EXPORT Reset_Handler
Reset_Handler	
	MOV R0,#16
	MOV R1,#8
	LDR R2,=RES
	
UP2	CMP R0,R1
	BEQ FINAL
	BCS UP
	SUB R1,R1,R0
	CMP R0,R1
	BNE UP2
	B FINAL
UP
	SUB R0,R0,R1
	CMP R0,R1
	BNE UP2
FINAL
	STR R0,[R2]
STOP B STOP

	AREA mydata, DATA, READWRITE
RES DCD 0x0
	END